/**
 * Generate an index file linking all per-finding reports.
 */
export function generateFindingsIndex(
  findings: BountyFindingData[],
  targetName: string,
): string {
  const lines: string[] = [];

  lines.push(`# Security Findings — ${targetName}`, '', `> ${findings.length} finding(s) identified by Shadow Auditor`, '', '| ID | Severity | Title | CWE |', '|----|----------|-------|-----|');

  for (const f of findings) {
    const cweNum = f.cweId.replace(/^CWE-?/i, '');
    const filename = `${f.vulnId}.md`;
    lines.push(`| [${f.vulnId}](${filename}) | ${f.severityLabel} | ${f.title} | CWE-${cweNum} |`);
  }

  lines.push('');

  return lines.join('\n');
}
