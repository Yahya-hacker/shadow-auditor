/**
 * Bounty Report Generator - Deterministic JSON-to-Markdown Templating Engine.
 *
 * THE LLM IS NEVER ALLOWED TO WRITE THE FINAL REPORT PROSE.
 *
 * It produces structured JSON data only. This module renders that data
 * into fixed Markdown templates, injecting raw sandbox execution logs
 * verbatim as cryptographic proof. The LLM cannot exaggerate, hallucinate,
 * or embellish the proof-of-concept section.
 */

import { type OastCallback, type SandboxExecResult } from '../dast/dast-schema.js';

// =============================================================================
// Types
// =============================================================================

export interface BountyFindingData {
  cweId: string;
  impactDescription: string;
  remediationSummary?: string;
  reproductionSteps: string[];
  severityLabel: string;
  summary: string;
  title: string;
  vulnId: string;
}

export interface BountyReportOptions {
  findings: BountyFindingData[];
  oastCallbacks?: OastCallback[];
  platform?: 'bugcrowd' | 'generic' | 'hackerone';
  sandboxLogs?: SandboxExecResult[];
  targetName: string;
}

// =============================================================================
// CWE / OWASP Link Helpers
// =============================================================================

function cweUrl(cweId: string): string {
  const num = cweId.replace(/^CWE-?/i, '');
  return `https://cwe.mitre.org/data/definitions/${num}.html`;
}

function owaspLink(cweId: string): string | undefined {
  const num = Number.parseInt(cweId.replace(/^CWE-?/i, ''), 10);

  // Map common CWEs to OWASP Top 10
  const map: Record<number, string> = {
    22: 'https://owasp.org/Top10/A01_2021-Broken_Access_Control/',
    78: 'https://owasp.org/Top10/A03_2021-Injection/',
    79: 'https://owasp.org/Top10/A03_2021-Injection/',
    89: 'https://owasp.org/Top10/A03_2021-Injection/',
    94: 'https://owasp.org/Top10/A03_2021-Injection/',
    200: 'https://owasp.org/Top10/A01_2021-Broken_Access_Control/',
    287: 'https://owasp.org/Top10/A07_2021-Identification_and_Authentication_Failures/',
    327: 'https://owasp.org/Top10/A02_2021-Cryptographic_Failures/',
    502: 'https://owasp.org/Top10/A08_2021-Software_and_Data_Integrity_Failures/',
    611: 'https://owasp.org/Top10/A05_2021-Security_Misconfiguration/',
    918: 'https://owasp.org/Top10/A10_2021-Server-Side_Request_Forgery/',
  };

  return map[num];
}

// =============================================================================
// Platform Headers
// =============================================================================

function platformHeader(platform: string, targetName: string): string {
  switch (platform) {
    case 'bugcrowd': {
      return [
        `# Security Assessment Report — ${targetName}`,
        `> Platform: Bugcrowd`,
        `> Generated by: Shadow Auditor (Autonomous)`,
        `> Date: ${new Date().toISOString().split('T')[0]}`,
        '',
      ].join('\n');
    }

    case 'hackerone': {
      return [
        `# Security Assessment Report — ${targetName}`,
        `> Platform: HackerOne`,
        `> Generated by: Shadow Auditor (Autonomous)`,
        `> Date: ${new Date().toISOString().split('T')[0]}`,
        '',
      ].join('\n');
    }

    default: {
      return [
        `# Security Assessment Report — ${targetName}`,
        `> Generated by: Shadow Auditor (Autonomous)`,
        `> Date: ${new Date().toISOString().split('T')[0]}`,
        '',
      ].join('\n');
    }
  }
}

