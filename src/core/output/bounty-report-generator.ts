// Per-Finding Template
// =============================================================================

/**
 * Generate a deterministic Markdown report for a single finding.
 *
 * The "Proof of Concept" section is populated EXCLUSIVELY from raw
 * SandboxExecResult logs. The LLM has no access to modify this section.
 */
export function generatePerFindingReport(
  finding: BountyFindingData,
  sandboxLogs: SandboxExecResult[],
  oastCallbacks: OastCallback[],
): string {
  const sections: string[] = [];
  const cweNum = finding.cweId.replace(/^CWE-?/i, '');

  // Header
  sections.push(`## [CWE-${cweNum}] ${finding.title}`, '', `**Severity:** ${finding.severityLabel} | **ID:** ${finding.vulnId}`, '', '### Summary', finding.summary, '', '### Steps to Reproduce');
  for (const [i, step] of finding.reproductionSteps.entries()) {
    sections.push(`${i + 1}. ${step}`);
  }

  sections.push('');

  // Proof of Concept (VERBATIM from sandbox logs — NEVER LLM-generated)
  if (sandboxLogs.length > 0) {
    sections.push('### Proof of Concept', '', '> ⚠️ The following commands and outputs were captured verbatim from', '> an isolated sandbox execution. They have NOT been modified by AI.', '');

    for (const log of sandboxLogs) {
      sections.push('```bash', `$ ${log.command}`);
      if (log.stdout.trim()) {
        sections.push(log.stdout.trim());
      }

      if (log.stderr.trim()) {
        sections.push(`# stderr:`, log.stderr.trim());
      }

      sections.push(`# exit code: ${log.exitCode} | duration: ${log.durationMs}ms`, '```', '');
    }
  }

  // OAST Callback Evidence
  if (oastCallbacks.length > 0) {
    sections.push('### OAST Callback Evidence', '', '| Timestamp | URL | Method |', '|-----------|-----|--------|');

    for (const cb of oastCallbacks) {
      sections.push(`| ${cb.timestamp} | \`${cb.url}\` | ${cb.method} |`);
    }

    sections.push('');
  }

  // Impact
  sections.push('### Impact', finding.impactDescription, '');

  // Suggested Remediation
  if (finding.remediationSummary) {
    sections.push('### Suggested Remediation', finding.remediationSummary, '');
  }

  // References
  sections.push('### References', `- ${cweUrl(finding.cweId)}`);

  const owasp = owaspLink(finding.cweId);
  if (owasp) {
    sections.push(`- ${owasp}`);
  }

  sections.push('');

  return sections.join('\n');
}

